var grade = "A";

switch(grade) {
	case "A":
		alert("Grade is an A.");
	break;
	case "B":
		alert("Grade is a B.");
	break;
	case "C":
		alert("Grade is a C.");
	break;
	case "D":
		alert("Grade is a D.");
	break;
	case "F":
		alert("Grade is a F.");
	break;
	default:
		alert("Not a valid grade.");
}